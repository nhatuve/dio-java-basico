public class Calculadora{
    /**
     * Este método é usado para somar dois números inteiros
     * @param numeroA este é o primeiro parâmetro do método
     * @param numeroB este é o segundo parâmetro do método
     * @return int o resultado deste método é a soma dos dois números
     */
    public int somar(int numeroA, int numeroB){
        return numeroA + numeroB;
        
    }
}